public class Laptop {
    private int id;
    private String name;
    private int ram;
    private int hdd;
    private String color;

    public Laptop(int id, String name, int ram, int hdd, String color) {
        this.id = id;
        this.name = name;
        this.ram = ram;
        this.hdd = hdd;
        this.color = color;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getRam() {
        return ram;
    }

    public void setRam(int ram) {
        this.ram = ram;
    }

    public int getHdd() {
        return hdd;
    }

    public void setHdd(int hdd) {
        this.hdd = hdd;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    @Override
    public String toString() {
        return "name: " + name + ", ram: " + ram + ", hdd: " + hdd + ", color: " + color;
    }
}
